// для работы с карточками
class Product{
    constructor(
        name,
        title,
        fontSize,
        productText
    ){
        this.name = name
        this.title = title
        this.fontSize = fontSize
        this.productText = productText
    }
    ToHtml(){
        return `<div style="font-size:${this.fontSize}" title="${this.title}" class="product-card">${this.name}</div>`
    }
    ToProductName(){
        return `${this.title}`
    }
    ToProductWindow(){
        return `${this.productText}`
    }
}

